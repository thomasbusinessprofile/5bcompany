// Generates supabase/migrations/0012_articles_seo_seed.sql from
// app/shared/article-seed.ts. Run with: node scripts/build-articles-sql.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const seedTs = fs.readFileSync(path.join(root, "app/shared/article-seed.ts"), "utf8");

// Strip TypeScript-only syntax so we can eval as plain JS.
const js = seedTs
  .replace(/^import .*?;\s*$/gm, "")
  .replace(/export type .*?\};\s*/gs, "")
  .replace(/: Article\[\]/g, "")
  .replace(/: Record<string, ArticleMeta>/g, "")
  .replace(/ as const/g, "")
  .replace(/export const /g, "globalThis.__seedExports.")
  .replace(/^/, "globalThis.__seedExports = {};\n")
  + "\nObject.assign(globalThis, globalThis.__seedExports);";

const fn = new Function(js + "\nreturn { seedArticles, articleMeta };");
const { seedArticles, articleMeta } = fn();

// Pick a unique dollar-quote tag that doesn't appear in any body.
function tagFor(text) {
  let i = 0;
  while (text.includes(`$b${i}$`)) i++;
  return `b${i}`;
}

function sqlString(s) {
  if (s === null || s === undefined) return "NULL";
  return `'${String(s).replace(/'/g, "''")}'`;
}

const rows = seedArticles.map((a) => {
  const meta = articleMeta[a.slug] ?? {};
  const tag = tagFor(a.body || "");
  return `(
  ${sqlString(a.slug)},
  ${sqlString(a.title)},
  ${sqlString(a.excerpt)},
  $${tag}$${a.body}$${tag}$,
  ${sqlString(a.keyword)},
  ${sqlString(a.seoTitle)},
  ${sqlString(a.seoDescription)},
  ${sqlString(a.status)},
  ${meta.publishedAt ? `'${meta.publishedAt}'::timestamptz` : "NULL"},
  ${sqlString(meta.category || null)},
  ${sqlString(meta.image || null)}
)`;
});

const sql = `-- Generated by scripts/build-articles-sql.mjs
-- Adds SEO fields (category, image_url) and upserts 21 curated insight articles.

begin;

alter table public.articles
  add column if not exists category text,
  add column if not exists image_url text;

create index if not exists articles_category_idx on public.articles (category);

insert into public.articles (
  slug, title, excerpt, body, keyword, seo_title, seo_description,
  status, published_at, category, image_url
) values
${rows.join(",\n")}
on conflict (slug) do update set
  title = excluded.title,
  excerpt = excluded.excerpt,
  body = excluded.body,
  keyword = excluded.keyword,
  seo_title = excluded.seo_title,
  seo_description = excluded.seo_description,
  status = excluded.status,
  published_at = coalesce(excluded.published_at, public.articles.published_at),
  category = excluded.category,
  image_url = excluded.image_url;

commit;
`;

const out = path.join(root, "supabase/migrations/0012_articles_seo_seed.sql");
fs.writeFileSync(out, sql);
console.log(`Wrote ${out} (${seedArticles.length} articles, ${sql.length} bytes)`);
